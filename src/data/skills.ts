export function getSkill(author: string, slug: string): Skill | undefined {
  return skills.find(s => s.author === author && s.slug === slug);
}

export function getRecentSkills(limit = 20): Skill[] {
  return [...skills]
    .sort((a, b) => b.added.localeCompare(a.added))
    .slice(0, limit);
}
